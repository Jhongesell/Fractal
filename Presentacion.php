<blockquote><b><font color=#FF0000>FRACTAL C++ </font>es un sistema de 
software que sirve para analizar visual e interactivamente, el comportamiento de 
los sistemas din&aacute;micos modelados con iteraciones de funciones. 
FRACTAL es un analizador de iteraci&oacute;n de funciones holomorfas (que act&uacute;an en el 
plano complejo) y permite, mediante la experimentaci&oacute;n 
computacional, estudiar sus diferentes comportamientos. Para este fin, actualmente 
la computadora se ha convertido en el laboratorio ideal de experimentaci&oacute;n.
<br><br>
FRACTAL constituye un prototipo de software desarrollado para asistir a la ense&ntilde;anza e investigaci&oacute;n de los sistemas din&aacute;micos 
modelados con iteraciones de funciones en el plano. As&iacute;, bajo un ambiente sencillo de operar, FRACTAL permite analizar un conjunto 
muy variado de familias de funciones holomorfas como z2+c, a*sin(z)+b+z, ez, entre otras, as&iacute; como los conjuntos de Mandelbrot 
asociados a estas funciones. Para cada familia de funciones se tienen dos posibilidades: la ventana que despliega la imagen del 
conjunto de Mandelbrot y la ventana que despliega (una vez que son fijados los par&aacute;metros convenientes) las regiones de atracci&oacute;n 
de cada atractor de la funci&oacute;n que son los llamados conjuntos de Fatou y 
Julia; las fronteras de &eacute;ste son los llamados conjuntos de Julia. 
As&iacute;, el software es especialmente &uacute;til para trabajar con sistemas din&aacute;micos que involucren par&aacute;metros para investigar los cambios 
cualitativos (bifurcaciones) que ocurren en la din&aacute;mica al variar los 
valores de &eacute;stos.


